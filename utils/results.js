/**
 * 算法：15 题答案 → 10 个维度得分 → 3 个二元维度判定 → 8 型
 */
function calculateResult(answers) {
  const raw = { sensitive: 0, rational: 0, assertive: 0, diplomatic: 0, structured: 0, spontaneous: 0, expressive: 0, reserved: 0, social_drain: 0, adventure: 0 }

  const MINI_QUESTIONS = require('./questions').MINI_QUESTIONS

  answers.forEach((answerIndex, qIdx) => {
    const q = MINI_QUESTIONS[qIdx]
    if (!q) return
    const opt = q.options[answerIndex]
    if (opt && opt.score) {
      Object.entries(opt.score).forEach(([k, v]) => { raw[k] = (raw[k] || 0) + v })
    }
  })

  // 3 个二元维度判定
  // 能量流向: 外源 (expressive + social 高, social_drain 低) vs 内源 (social_drain + reserved 高)
  const outwardScore = (raw.expressive || 0) + (raw.diplomatic || 0) - (raw.social_drain || 0) - (raw.reserved || 0) * 0.5
  const energy = outwardScore > 0 ? 'outward' : 'inward'

  // 判断频道: 脑派 (rational + structured 高) vs 心派 (sensitive + spontaneous 高)
  const logicScore = (raw.rational || 0) + (raw.structured || 0) - (raw.sensitive || 0) - (raw.spontaneous || 0)
  const judgment = logicScore > 0 ? 'logic' : 'feel'

  // 生活节奏: 建构 (structured + assertive 高) vs 流动 (spontaneous + adventure 高)
  const orderScore = (raw.structured || 0) + (raw.assertive || 0) * 0.6 - (raw.spontaneous || 0) - (raw.adventure || 0)
  const rhythm = orderScore > 0 ? 'order' : 'flow'

  const typeKey = `${energy}_${judgment}_${rhythm}`
  const typeMap = {
    'outward_logic_order': 'outward_logic_order',
    'outward_logic_flow': 'outward_logic_flow',
    'outward_feel_order': 'outward_feel_order',
    'outward_feel_flow': 'outward_feel_flow',
    'inward_logic_order': 'inward_logic_order',
    'inward_logic_flow': 'inward_logic_flow',
    'inward_feel_order': 'inward_feel_order',
    'inward_feel_flow': 'inward_feel_flow'
  }

  const mappedType = typeMap[typeKey] || 'inward_feel_flow'
  return RESULT_TEMPLATES[mappedType]
}

